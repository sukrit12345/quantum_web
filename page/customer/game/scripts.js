const MAX_STICK_NUMBER = Object.keys(FORTUNE_MESSAGES).length;
const SHAKE_COST = 100; // ใช้ 100 Coin ต่อการเสี่ยง 1 ครั้ง

let playerCoins = 0;
let isShaking = false;

// --- DOM Elements ---
const coinCountDisplay = document.getElementById("coinCount");
const fortuneCylinder = document.getElementById("fortuneCylinder");
const shakeButton = document.getElementById("shakeButton");
const coinErrorDisplay = document.getElementById("coinError");
const resultArea = document.getElementById("resultArea");
const stickNumberDisplay = document.getElementById("stickNumberDisplay");
const fortunePaper = document.getElementById("fortunePaper");
const settingsButton = document.getElementById("settingsButton");

// ดึง id_card_number จาก localStorage
const id_card_number = localStorage.getItem("idCardNumber");

if (!id_card_number) {
    alert("ไม่พบข้อมูลผู้ใช้งาน กรุณาเข้าสู่ระบบก่อน");
    shakeButton.disabled = true;
    window.location.href = '../../../login/index.html';
}

// โหลดเหรียญจาก API
async function fetchCoinsFromAPI() {
    try {
        const response = await window.makeAuthenticatedApiRequest('/auth/me', 'GET');
        if (response && typeof response.coinBalance === "number") {
            playerCoins = response.coinBalance;
            updateCoinDisplay();
            localStorage.setItem('userBalance', playerCoins);
        } else {
            throw new Error("รูปแบบข้อมูลเหรียญไม่ถูกต้องจาก API");
        }
    } catch (error) {
        console.error("โหลดเหรียญล้มเหลว:", error.message);
        playerCoins = 0;
        updateCoinDisplay();
        alert(`ไม่สามารถโหลดเหรียญได้: ${error.message}`);
        if (error.message.includes('token failed') || error.message.includes('Not authorized')) {
            localStorage.clear();
            window.location.href = '../../../login/index.html';
        }
    }
}

function updateCoinDisplay() {
    coinCountDisplay.textContent = playerCoins.toFixed(0); // แสดงจำนวนเต็ม
    shakeButton.disabled = playerCoins < SHAKE_COST || isShaking;
    coinErrorDisplay.classList.toggle("hidden", playerCoins >= SHAKE_COST || isShaking);
}

async function shakeAndGetFortune() {
    if (isShaking) return;
    if (playerCoins < SHAKE_COST) {
        alert(`เหรียญไม่พอ ต้องใช้ ${SHAKE_COST} Coin ต่อการเสี่ยง`);
        return;
    }

    isShaking = true;
    shakeButton.textContent = "กำลังเขย่า...";
    resultArea.classList.add("hidden");
    fortuneCylinder.classList.add("shaking");

    try {
        // เรียก API เล่นเกม
        const response = await window.makeAuthenticatedApiRequest('/customer/game/play', 'POST', { costPerPlay: SHAKE_COST });

        if (response && typeof response.newCoinBalance === "number" && response.fortuneResult) {
            playerCoins = response.newCoinBalance;
            updateCoinDisplay();

            let stickNumber;
            // หาเลขใบจากคำทำนาย
            for (const key in FORTUNE_MESSAGES) {
                if (FORTUNE_MESSAGES[key].general === response.fortuneResult || FORTUNE_MESSAGES[key].title === response.fortuneResult) {
                    stickNumber = parseInt(key);
                    break;
                }
            }
            if (!stickNumber) {
                stickNumber = Math.floor(Math.random() * MAX_STICK_NUMBER) + 1;
            }

            setTimeout(() => {
                fortuneCylinder.classList.remove("shaking");
                displayFortune(stickNumber);
                shakeButton.textContent = `ทำนายอีกครั้ง (${SHAKE_COST} Coin)`;
                isShaking = false;
                updateCoinDisplay();
            }, 1500);
        } else {
            throw new Error("รูปแบบข้อมูลคำทำนายไม่ถูกต้องจาก API");
        }
    } catch (error) {
        console.error("เกิดข้อผิดพลาดในการเสี่ยง:", error);
        alert(`เกิดข้อผิดพลาด: ${error.message}`);
        fortuneCylinder.classList.remove("shaking");
        shakeButton.textContent = `ทำนายอีกครั้ง (${SHAKE_COST} Coin)`;
        isShaking = false;
        updateCoinDisplay();
    }
}

function displayFortune(number) {
    stickNumberDisplay.textContent = number;
    const fortune = FORTUNE_MESSAGES[number];

    if (fortune) {
        fortunePaper.innerHTML = `
        <h3 class="text-xl font-bold text-center text-red-700 mb-3">${fortune.title}</h3>
        <p class="mb-2"><strong class="text-gray-700">ดวงชะตาทั่วไป:</strong> ${fortune.general}</p>
        <p class="mb-2"><strong class="text-gray-700">การงาน:</strong> ${fortune.work}</p>
        <p class="mb-2"><strong class="text-gray-700">การเงิน:</strong> ${fortune.money}</p>
        <p class="mb-2"><strong class="text-gray-700">ความรัก:</strong> ${fortune.love}</p>
        <p><strong class="text-gray-700">สุขภาพ:</strong> ${fortune.health}</p>
        `;
    } else {
        fortunePaper.innerHTML = '<p class="text-center text-red-500">ไม่พบคำทำนายสำหรับหมายเลขนี้</p>';
    }

    resultArea.classList.remove("hidden");
    resultArea.scrollIntoView({ behavior: "smooth", block: "start" });
}

// Event listeners
shakeButton.addEventListener("click", shakeAndGetFortune);
fortuneCylinder.addEventListener("click", shakeAndGetFortune);

if (settingsButton) {
    settingsButton.addEventListener("click", () => alert("ยังไม่มีหน้าตั้งค่า"));
}

